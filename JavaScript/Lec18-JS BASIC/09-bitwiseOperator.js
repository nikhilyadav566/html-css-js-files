//  Basically  There are two types of Bitwise Operator.

//   1. Bitwise AND (&)                           2. Bitwise OR (|)

//  Bits:- 
//         0:- zero means false.
//         1:- one means true.


let a = 2;
let b = 3 ;
console.log(a & b);      // Output : -> 2
console.log(a | b);      //  3
