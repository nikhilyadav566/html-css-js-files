// 1. If-elseif-else
// syntax:-
//          if(condition){
//                   statements   
//             }
//          else if(condition){ 
//                  statements
//             }
//          else{   statements
//                      }

let marks=65;

if(marks>=90){
    console.log('A Grade');
}
else if(marks>=80){
    console.log('B Grade');
}
else if(marks>=70){
    console.log('C Grade');
}
else if(marks>=60){
    console.log('D Grade');
}
else if(marks>=50){
    console.log('E Grade');
}
else{
    console.log('fail')
}

//  Output :->  D Grade
