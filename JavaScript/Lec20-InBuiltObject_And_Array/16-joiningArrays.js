//   Joining Arrays
//                  join():-> join method in JavaScript to convert an array into a string.

let arr = [21,45,85,75,'Nikhil'];   // The array arr contains elements of different data types: [21, 45, 85, 75, 'Nikhil']


let value = arr.join(',');
console.log(value);    //  Output : 21,45,85,75,Nikhil

//  join :-> The join(',') method joins all the elements of the array into a single string, using a comma , as the separator.
