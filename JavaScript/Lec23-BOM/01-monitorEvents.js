// BOM (Browser Object Model)

// The Browser Object Model (BOM) allows JavaScript
// to interact with the browser window.

// Examples:
// - window
// - document
// - history
// - location
// - navigator


monitorEvents()

// monitorEvents() is a Chrome DevTools function.

// Purpose:
// Starts monitoring events on an element and prints
// them in the Console whenever they occur.

// Syntax:
monitorEvents(target)

// Example:
monitorEvents(document);

// Now try:
// - Clicking anywhere on the page
// - Pressing keyboard keys
// - Scrolling the page

// The Console will display the events.

// Note:
// This function works only in the browser Console,
// not inside a normal JavaScript file